package org.notariado.inti.exceptions;

public class RecepcionException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -3149562934045818317L;

	/**
	 * 
	 */

	public RecepcionException() {
		super();
	}

	public RecepcionException(String message, Throwable cause) {
		super(message, cause);
	}

	public RecepcionException(String message) {
		super(message);
	}
	

}
